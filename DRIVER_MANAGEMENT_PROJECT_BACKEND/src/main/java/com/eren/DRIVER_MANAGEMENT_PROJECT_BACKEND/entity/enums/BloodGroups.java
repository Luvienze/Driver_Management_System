package com.eren.DRIVER_MANAGEMENT_PROJECT_BACKEND.entity.enums;

/**
 * Represents the possible human blood groups.
 */
public enum BloodGroups {
    /** Blood group A Rh positive (A+). */
    A_POSITIVE,
    /** Blood group A Rh negative (A-). */
    A_NEGATIVE,
    /** Blood group B Rh positive (B+). */
    B_POSITIVE,
    /** Blood group B Rh negative (B-). */
    B_NEGATIVE,
    /** Blood group AB Rh positive (AB+). */
    AB_POSITIVE,
    /** Blood group AB Rh negative (AB-). */
    AB_NEGATIVE,
    /** Blood group O Rh positive (O+). */
    O_POSITIVE,
    /** Blood group O Rh negative (O-). */
    O_NEGATIVE
}
